package chapter2;

public class BooleanExample {

	public static void main(String[] args) {
		
		// 논리형
		// boolean 형은 두가지 값만 존재 : true, false
		
		boolean stop = true;
		
		if(stop) { // 중지합니다.
			System.out.println("중지합니다.");
		} else {
			System.out.println("시작합니다.");
		}
	

		if(!stop) { // 시작합니다.
			System.out.println("중지합니다.");
		} else {
			System.out.println("시작합니다.");
		}
		
		
		
		
		
		
		
		
		
	}	

}
