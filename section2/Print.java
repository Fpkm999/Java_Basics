package section2;

public class Print {
	public static void main(String[] args) {
		System.out.print("Hello");	// print : 줄바꿈 없이 출력
		System.out.print("JAVA");
		
		System.out.println(); // 줄바꿈이 된다. 왜냐하면 괄호안에 null을 출력하고 행을 바꾸기 때문
	
		System.out.println("Hello");	// println : 출력을 하고 행을 바꿈
		System.out.println("JAVA");		// ln : line 라인을 줄인 말임
		
	}
}
